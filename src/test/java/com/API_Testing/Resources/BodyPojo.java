package com.API_Testing.Resources;
public class BodyPojo 
{
	private String id;
	private String employeeName;
	private String employeAge;
	private String employeProfession;
	private String employeGender;
	private String address;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	public String getEmployeAge() {
		return employeAge;
	}
	public void setEmployeAge(String employeAge) {
		this.employeAge = employeAge;
	}
	public String getEmployeProfession() {
		return employeProfession;
	}
	public void setEmployeProfession(String employeProfession) {
		this.employeProfession = employeProfession;
	}
	public String getEmployeGender() {
		return employeGender;
	}
	public void setEmployeGender(String employeGender) {
		this.employeGender = employeGender;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
	

}
